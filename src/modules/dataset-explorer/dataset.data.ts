// Interfaces para la estructura de datos
export interface ContactInfo {
  email: string
  phone: string
  mobile?: string
  extension?: string
}

export interface Address {
  street: string
  city: string
  state: string
  zipCode: string
  country: string
}

export interface Department {
  id: string
  name: string
  code: string
  manager?: string
  budget?: number
  location?: string
}

export interface Position {
  title: string
  level:
    | 'Junior'
    | 'Mid'
    | 'Senior'
    | 'Lead'
    | 'Manager'
    | 'Director'
    | 'VP'
    | 'C-Level'
  department: string
  salary?: number
  startDate: string
  isRemote?: boolean
}

export interface Skills {
  technical: string[]
  soft: string[]
  languages: string[]
  certifications: string[]
}

export interface Project {
  id: string
  name: string
  role: string
  startDate: string
  endDate?: string
  status: 'Active' | 'Completed' | 'On Hold' | 'Cancelled'
  budget?: number
  technologies: string[]
}

export interface Performance {
  rating: number
  lastReview: string
  achievements: string[]
  areasForImprovement: string[]
  nextReviewDate: string
}

export interface Employee {
  id: string
  personalInfo: {
    firstName: string
    lastName: string
    middleName?: string
    dateOfBirth: string
    gender: 'Male' | 'Female' | 'Non-binary' | 'Other'
    nationality: string
    maritalStatus: 'Single' | 'Married' | 'Divorced' | 'Widowed'
  }
  contact: ContactInfo
  address: Address
  position: Position
  skills: Skills
  projects: Project[]
  performance: Performance
  emergencyContact?: {
    name: string
    relationship: string
    phone: string
  }
  benefits?: {
    healthInsurance: boolean
    dentalInsurance: boolean
    visionInsurance: boolean
    retirementPlan: boolean
    stockOptions: boolean
  }
  metadata: {
    hireDate: string
    employeeNumber: string
    isActive: boolean
    lastUpdated: string
  }
}

